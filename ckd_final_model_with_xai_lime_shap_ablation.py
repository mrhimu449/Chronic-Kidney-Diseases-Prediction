def build_ablation_pipeline(remaining_features):
    """
    Rebuild the preprocessing + tuned final estimator for a reduced raw feature set.
    """
    remaining_features = list(remaining_features)

    numeric_subset = [
        feature for feature in numeric_features
        if feature in remaining_features
    ]
    categorical_subset = [
        feature for feature in categorical_features
        if feature in remaining_features
    ]

    transformers = []

    if numeric_subset:
        numeric_transformer_ablation = Pipeline(steps=[
            ("imputer", SimpleImputer(strategy="median")),
            ("scaler", StandardScaler())
        ])
        transformers.append(("num", numeric_transformer_ablation, numeric_subset))

    if categorical_subset:
        categorical_transformer_ablation = Pipeline(steps=[
            ("imputer", SimpleImputer(strategy="most_frequent")),
            ("onehot", OneHotEncoder(handle_unknown="ignore"))
        ])
        transformers.append(("cat", categorical_transformer_ablation, categorical_subset))

    if not transformers:
        raise ValueError("Ablation scenario removed all usable features.")

    ablation_preprocessor = ColumnTransformer(transformers=transformers)

    # Clone the tuned final estimator so each ablation is retrained independently.
    tuned_model_clone = clone(publication_final_pipeline.named_steps["model"])

    return Pipeline(steps=[
        ("preprocessor", ablation_preprocessor),
        ("model", tuned_model_clone)
    ])

