def extract_positive_class_shap_values(raw_shap_values, class_position):
    # Return SHAP values for the CKD class across common SHAP output formats.
    if isinstance(raw_shap_values, list):
        return np.asarray(raw_shap_values[class_position])

    shap_array = np.asarray(raw_shap_values)

    if shap_array.ndim == 3:
        # Common newer SHAP shape: samples x features x classes
        if shap_array.shape[-1] > class_position:
            return shap_array[:, :, class_position]

        # Alternate shape: classes x samples x features
        if shap_array.shape[0] > class_position:
            return shap_array[class_position, :, :]

    return shap_array

def extract_positive_expected_value(raw_expected_value, class_position):
    # Return SHAP expected value for the CKD class.
    expected_array = np.asarray(raw_expected_value)
    if expected_array.ndim == 0:
        return float(expected_array)
    if expected_array.size > class_position:
        return float(expected_array[class_position])
    return float(expected_array.ravel()[0])

