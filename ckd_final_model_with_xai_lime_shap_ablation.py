"""## Ablation Study: Feature Removal Sensitivity of the Tuned Final Model

This section tests how much the selected tuned final model depends on specific input features.  
To avoid choosing ablation targets from the held-out test set, features are ranked by **training-set permutation importance**. The model is then retrained after:

- removing the top 1, top 3, and top 5 training-ranked features,
- removing each of the five highest-ranked features individually,
- removing all numeric features, and
- removing all categorical features.

Each ablated model is evaluated on the same untouched test split used in the final report. Larger drops in ROC-AUC, F1, or balanced accuracy indicate stronger dependence on the removed feature(s).
"""

