def discretize_column(series, n_bins=4):
    """
    Converts numeric columns into quantile bins.
    Keeps categorical columns as categories.
    """
    s = series.copy()

    if pd.api.types.is_numeric_dtype(s):
        s = pd.to_numeric(s, errors="coerce")

        if s.notna().sum() == 0:
            return pd.Series(["missing"] * len(s), index=s.index)

        s = s.fillna(s.median())

        unique_count = s.nunique()

        if unique_count <= 1:
            return pd.Series(["constant"] * len(s), index=s.index)

        bins = min(n_bins, unique_count)

        try:
            return pd.qcut(s, q=bins, duplicates="drop").astype(str)
        except Exception:
            return pd.cut(s, bins=bins, duplicates="drop").astype(str)

    else:
        return (
            s.fillna("missing")
            .astype(str)
            .str.strip()
            .replace({"": "missing", "nan": "missing"})
        )

